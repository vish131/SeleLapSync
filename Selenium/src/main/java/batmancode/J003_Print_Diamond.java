package batmancode;

public class J003_Print_Diamond {

	public static void main(String[] args) {

		for(int i=1;i<5;i++) {

			for(int j=i;j<5;j++) {
				System.out.print("-");
			}
			for(int K=i;K>0;K--) {
				System.out.print("*");}

			for(int M=i-1;M>0;M--) {
				System.out.print("*");}

			System.out.println();
		}
//below are to repeat down
		
		
		
		
		
		}
		
	}


